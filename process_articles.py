def process_article(filepath):
    """处理单篇文章：优化标题 + 插入引流卡片"""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    lines = content.split('\n')
    if not lines:
        return False

    # 获取原标题（第一行，去除#和空格）
    original_title_line = lines[0]
    original_title = original_title_line.lstrip('#').strip()

    # 查找优化后的标题
    new_title = TITLE_MAP.get(original_title)
    if not new_title:
        # 尝试用文件名匹配
        basename = os.path.splitext(os.path.basename(filepath))[0]
        new_title = TITLE_MAP.get(basename)

    if not new_title:
        print(f"  ⚠️ 未找到映射，跳过: {original_title}")
        return False

    # 替换标题
    lines[0] = f"# {new_title}"

    # 查找配图位置（通常是第2-4行）
    image_line_idx = -1
    for i in range(1, min(len(lines), 6)):
        if lines[i].strip().startswith('![') and '](' in lines[i]:
            image_line_idx = i
            break

    # 构建新内容
    new_lines = []

    # 添加标题
    new_lines.append(lines[0])

    # 如果有配图，先加配图，再加开头引流卡片
    if image_line_idx > 0:
        # 复制到配图行（含）
        for i in range(1, image_line_idx + 1):
            new_lines.append(lines[i])
        # 空行 + 开头引流卡片
        new_lines.append('')
        new_lines.append(HEADER_CARD.strip())
        # 从配图后一行继续
        start_idx = image_line_idx + 1
    else:
        # 没有配图，直接加开头卡片
        new_lines.append('')
        new_lines.append(HEADER_CARD.strip())
        start_idx = 1

    # 复制文章主体
    for i in range(start_idx, len(lines)):
        new_lines.append(lines[i])

    # 检查末尾是否已有分隔线或类似内容
    # 在文章末尾添加引流卡片（确保不重复添加）
    footer_marker = "扫码关注程序员臻叔"
    if footer_marker not in content:
        new_lines.append('')
        new_lines.append(FOOTER_CARD.strip())

    new_content = '\n'.join(new_lines)

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)

    print(f"  ✅ {original_title}")
    print(f"     → {new_title}")
    return True


def main():
    articles_dir = '/Volumes/dz/code/kepu-book/articles'
    processed = 0
    skipped = 0

    for root, dirs, files in os.walk(articles_dir):
        for filename in files:
            if not filename.endswith('.md'):
                continue
            filepath = os.path.join(root, filename)
            basename = os.path.splitext(filename)[0]

            print(f"处理: {filepath}")

            if basename not in TITLE_MAP:
                print(f"  ⚠️ 未找到标题映射，跳过: {basename}")
                skipped += 1
                continue

            if process_article(filepath):
                processed += 1
            else:
                skipped += 1

    print(f"\n{'='*50}")
    print(f"处理完成: {processed} 篇")
    print(f"跳过: {skipped} 篇")


if __name__ == '__main__':
    main()
